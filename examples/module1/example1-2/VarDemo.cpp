// Использование переменной.

#include <iostream>

using namespace std;

int main()
{
    int length;     // здесь объявляется переменная.
    length = 7;     // здесь length присваивается значение 7.

    cout << "Длина равна ";
    cout << length; // здесь выводится 7;
    cout << endl;
    return 0;
}
